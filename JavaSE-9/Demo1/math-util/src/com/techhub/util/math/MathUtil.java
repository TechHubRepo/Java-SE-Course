package com.techhub.util.math;

/**
 * The MathUtil
 * 
 * @author ramniwash
 *
 */
public final class MathUtil {

	/**
	 * Add the two number and return the sum of them
	 * @param x the int number  a
	 * @param y the int number b
	 * @return int the sum
	 */
	public static final int add(int x, int y) {
		return x + y;
	}
	
	/**
	 * Subtract the two number and return the difference of them
	 * @param x the int number  a
	 * @param y the int number b
	 * @return int the sum
	 */
	public static final int substract(int x, int y) {
		return x - y;
	}
}
